package Model;

/**
 *
 * @author Admin
 */
public class Product {
     private String id, pname, price, imagePath, status;

    public Product(String id, String pname, String price, String imagePath, String status) {
        this.id = id;
        this.pname = pname;
        this.price = price;
        this.imagePath = imagePath;
        this.status = status;
    }

    public Product() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getImagePath() {
        return imagePath;
    }

    public void setImagePath(String imagePath) {
        this.imagePath = imagePath;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
    
}
